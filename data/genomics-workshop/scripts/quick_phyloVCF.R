USER=Sys.getenv(c("USER"))
PATH=paste("/scratch2/",USER,"/genomics-workshop",sep="")
LIB=paste(PATH,"/rlibs",sep="")
VCF=paste(PATH,"/SNPs/combined.vcf",sep="")
PDF=paste(PATH,"/phylo.pdf",sep="")

install.packages("vcfR",lib=LIB, repos="https://cran.r-project.org")
install.packages("ape", lib=LIB, repos="https://cran.r-project.org")
library(ape)
library(vcfR)

vcf <- read.vcfR(VCF, verbose = FALSE )
snp_matrix<-vcfR2genind(vcf)
stree = nj(dist.gene(t(snp_matrix)))
pdf(PDF, width=8, height=11)
plot(stree)
dev.off()
